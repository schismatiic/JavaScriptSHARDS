// El destructuring en JavaScript es una forma de extraer valores de arrays o propiedades de objetos y asignarlos a
// variables de manera más corta y legible, en lugar de hacerlo accediendo uno por uno. Básicamente “desempaquetas” los valores.
const numeros = [10, 20, 30];

// Con destructuring
const [x, y, z] = numeros;

console.log(x, y, z); // 10 20 30
